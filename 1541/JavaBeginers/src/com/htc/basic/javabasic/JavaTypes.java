package com.htc.basic.javabasic;

public class JavaTypes {

	public static void main(String args[]) {
		byte byteType = 100;
		int intType = 128000;
		short shortType = 10000;
		float floatType = 12345678.4f;
		double doubleType = 1.234e2;
		char charStype = 'C';
		long longType = 1234567890123456L;
		boolean booleanType = false;
		System.out.println(byteType);
		System.out.println(intType);
		System.out.println(shortType);
		System.out.println(floatType);
		System.out.println(doubleType);
		System.out.println(charStype);
		System.out.println(longType);
		System.out.println(booleanType);
	}

}
